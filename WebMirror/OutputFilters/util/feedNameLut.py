def patch_blogspot(innetloc):
	# Blogspot domains are coerced to ".com" since they seem to localize their TLD,
	# and somehow it all points to the same place in the end.
	if ".blogspot." in innetloc and not innetloc.endswith(".blogspot.com"):
		prefix = innetloc.split(".blogspot.")[0]
		innetloc = prefix + ".blogspot.com"
	return innetloc

def check_blogspot():
	netlocs = set()
	for nl in mapper.keys():
		patched = patch_blogspot(nl)
		if not patched in netlocs:
			netlocs.add(patched)
		else:
			raise RuntimeError("Duplicate netlocs in LUT! %s -> %s" % (nl, patched))

check_blogspot()



def getNiceName(srcurl, netloc=None, debug=False):
	if netloc:
		srcnetloc = netloc
	else:
		srcnetloc = urllib.parse.urlparse(srcurl).netloc

	orig = srcnetloc

	srcnetloc = patch_blogspot(srcnetloc)

	if debug:
		print("Netlocs: ", (orig, srcnetloc, "Not in Lookup" if not srcnetloc in mapper else mapper[srcnetloc]))

	if srcnetloc in mapper:
		return mapper[srcnetloc]

	return False
